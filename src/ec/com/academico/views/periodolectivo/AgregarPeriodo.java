    public void guardar() {
        ListPerLect = plc.findPeriodoLectivoEntities();
        for (int i = 0; i < ListPerLect.size(); i++) {
            try {
                objPer = ListPerLect.get(i);
                objPer.setEstado('I');
                plc.edit(objPer);
            } catch (Exception e) {
            }
        }
//        guardarPer.guardarPeriodoInactivo();
        try {
            objPer.setPeriodo(TxtPeriodo.getText());
            objPer.setEstado('A');

            plc.create(objPer);
            JOptionPane.showMessageDialog(this, "EXITO AL GUARDAR");
            setVisible(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
