    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txt_nombre_completo.getText().equals("") || txt_celular.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Complete los campos");
        } else {
            guardar();
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    public void guardar() {
        try {
            Contacto.setIdEstudiante(estudiantes);
            Contacto.setNombreCompleto(txt_nombre_completo.getText());
            Contacto.setDireccionDomiciliaria(txta_direccion.getText());
            Contacto.setTelefono(txt_telefono.getText());
            Contacto.setCelular(txt_celular.getText());
            Contacto.setEmail(txt_correo.getText());
            Contacto.setEstado('A');

            ce.create(Contacto);
            JOptionPane.showMessageDialog(this, "EXITO AL GUARDAR");
            setVisible(false);
            ConsultaEstudiantesContacto Ce = new ConsultaEstudiantesContacto(new javax.swing.JFrame(), true);
            Ce.setVisible(true);
        } catch (Exception e) {
        }
    }

