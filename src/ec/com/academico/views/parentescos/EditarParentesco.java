    public void guardar() {
        char estado;
        if (cbx_estado.getSelectedItem().equals("A")) {
            estado = 'A';
        } else {
            estado = 'I';
        }
        try {
            parentesco.setNombre(txt_nombres.getText());
            parentesco.setEstado('A');

            dc.edit(parentesco);
            JOptionPane.showMessageDialog(this, "EXITO AL GUARDAR");
            setVisible(false);

        } catch (Exception e) {
        }

    }
