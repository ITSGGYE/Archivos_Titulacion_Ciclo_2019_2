    public void guardar() {
        boolean valor1 = ValidarDTO.ValidarParalelo(Txt_Paralelo.getText());
        if (valor1 == true) {
            JOptionPane.showMessageDialog(this, "El Paralelo ya existente");
        } else {
            try {
                objParalelos.setNombre(Txt_Paralelo.getText());
                objParalelos.setEstado('A');

                pc.create(objParalelos);
                JOptionPane.showMessageDialog(this, "EXITO AL GUARDAR");
                setVisible(false);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
