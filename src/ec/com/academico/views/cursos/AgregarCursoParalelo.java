    public void cargarCbxParalelos() {
        listPar = pc.findParalelosEntities();
        for (int i = 0; i < listPar.size(); i++) {
            if (listPar.get(i).getEstado().equals('A')) {
                Cbx_paralelos.addItem(listPar.get(i).getNombre());
            }
        }
    }

    public void cargarCbxCursos() {
        listCur = cc.findCursosEntities();
        for (int i = 0; i < listCur.size(); i++) {
            if (listCur.get(i).getEstado().equals('A')) {
                Cbx_cursos.addItem(listCur.get(i).getNombre());
            }
        }
    }

