    public void guardar() {
        Paralelos paralelos = ObtenerDTO.ObtenerParalelos(Cbx_paralelos.getSelectedItem().toString());
        Cursos cursos = ObtenerDTO.ObtenerCursos(Cbx_cursos.getSelectedItem().toString());
        boolean valor1 = ValidarDTO.ValidarCursoParalelo(cursos.getIdCursos(), paralelos.getIdParalelos());
        if (valor1 == true) {
            JOptionPane.showMessageDialog(this, "Ya existente");
        } else {
//            if (txt_Minimo.getText() != null || txt_Maximo.getText() != null) {
            if (!"".equals(txt_Minimo.getText().toString()) && !"".equals(txt_Maximo.getText().toString())) {
                try {

                    objRelCurPar.setIdCurso(cursos);
                    objRelCurPar.setIdParalelo(paralelos);
                    objRelCurPar.setMinino(Integer.valueOf(txt_Minimo.getText()));
                    objRelCurPar.setMaximo(Integer.valueOf(txt_Maximo.getText()));
                    objRelCurPar.setEstado('A');
                    rc.create(objRelCurPar);
                    JOptionPane.showMessageDialog(this, "EXITO AL GUARDAR");
                    setVisible(false);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Llene los campos");
            }
        }

    }
