    public Cursos devuelveObjeto(Long id, List<Cursos> lista) {
        Cursos doc = null;
        for (int i = 0; i < lista.size(); i++) {
            if (Objects.equals(lista.get(i).getIdCursos(), id)) {
                doc = lista.get(i);
                break;
            }
        }
        return doc;
    }
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int id = 0;
        if (TbaRepresentante.getSelectedRow() >= 0) {
            /*hgfrt*/
            id = TbaRepresentante.getSelectedRow();
            curso = devuelveObjeto(Long.valueOf(TbaRepresentante.getValueAt(id, 0).toString()), listCur);
            if (curso != null) {
                EditarCurso Ce = new EditarCurso(new javax.swing.JFrame(), true, curso);
                Ce.setVisible(true);
                cargarTbaCursos();
            }
        } else {
            JOptionPane.showMessageDialog(null, "SELECCIONE UN CURSO PARA EDITAR");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void TbaRepresentanteMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbaRepresentanteMousePressed
        int id = 0;
        if (evt.getClickCount() == 2) {

            id = TbaRepresentante.getSelectedRow();

            curso = devuelveObjeto(Long.valueOf(TbaRepresentante.getValueAt(id, 0).toString()), listCur);
            if (curso != null) {
                EditarCurso Ce = new EditarCurso(new javax.swing.JFrame(), true, curso);
                Ce.setVisible(true);
            }
            cargarTbaCursos();
        }
    }//GEN-LAST:event_TbaRepresentanteMousePressed

