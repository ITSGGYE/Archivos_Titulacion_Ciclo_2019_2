    private void tbaPersonasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbaPersonasMousePressed
        int id = 0;
        if (evt.getClickCount() == 2) {
            
            id = tbaPersonas.getSelectedRow();
            
            personas = devuelveObjeto(Long.valueOf(tbaPersonas.getValueAt(id, 0).toString()), listUsu);
            if (personas != null) {
//                bodega_editar ep = new bodega_editar(new javax.swing.JFrame(), true, bodegaL, usu, emp, suc);
//                ep.
                setObj(personas);
                setVisible(false);
                
            }
        }
    }//GEN-LAST:event_tbaPersonasMousePressed
    public Usuarios devuelveObjeto(Long id, List<Usuarios> listabod) {
        Usuarios doc = null;
        for (int i = 0; i < listabod.size(); i++) {
            if (Objects.equals(listabod.get(i).getIdUsuario(), id)) {
                doc = listabod.get(i);
                break;
            }
        }
        return doc;
    }
    
