    public void guardar() {
        if (txtUsuario.getText().length() <= 5) {
            JOptionPane.showMessageDialog(this, "Debe tener al menos 6 caracteres");
        } else {       
                boolean valor1 = ValidarDTO.ValidarUsuario(txtUsuario.getText());
                if (valor1 == true) {
                    JOptionPane.showMessageDialog(this, "El Usuario ya existente");
                } else {
                    if (txtClave.getText().equals(txtClaveConfirm.getText())) {
                    Roles idRol = ObtenerDTO.ObtenerRol(1);/*id rol quemado*/

                    usu.setUsuario(txtUsuario.getText());
                    usu.setIdUsuario(objPersonas);
                    usu.setFechaCreacion(fechaActual);
                    usu.setIdRol(idRol);
                    usu.setEstado('A');
                    usu.setContraseña(aes.encrypt(txtClave.getText()));
                    try {
                        pc.create(usu);
                        JOptionPane.showMessageDialog(this, "EXITO AL GUARDAR");
                        setVisible(false);
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, e.getMessage());
                    }
                }
            }
        }
    }

