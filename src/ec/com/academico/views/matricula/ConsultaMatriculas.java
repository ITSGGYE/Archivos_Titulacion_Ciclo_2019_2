    public void buscar() {
        String query = "";

        query = txtBuscar.getText();

        int pos = cbxFiltro.getSelectedIndex();
        if (pos == 0) {
            JOptionPane.showMessageDialog(null, "Seleccion una Opcion");
        }
        if (pos == 1) {
            //NÂ° MATRICULA
            listMatric = mc.findMatriculaEntities();
            Tablas.listarMatriculasNumMatricula(listMatric, TbaMatriculados, query);

        }
        if (pos == 2) {
            //PERIODO LECTIVO
            listMatric = mc.findMatriculaEntities();
            Tablas.listarMatriculasPeriodoLectivo(listMatric, TbaMatriculados, query);
        }
        if (pos == 3) {
            //ESTUDIANTE
            listMatriculaJoin = selectMatricula.listarMatriculaEstu(query);
//        ListProdVent = selectProdVent.listarProductoVenta();
            Tablas.listarMatriculasEstudiante(listMatriculaJoin, TbaMatriculados);
        }
        if (pos == 4) {
            //Curso
//            query  = query.substring(5,10);  
            try {
                String curso = " ";
                String paralelo = " ";
                String[] arrSplit_2 = query.split(" ", 2);
                for (int i = 0; i < arrSplit_2.length; i++) {
                    curso = arrSplit_2[0];
                    paralelo = arrSplit_2[1];
                }
//            String curso = query.substring(0,5);
//            String paralelo = query.substring(6,9);
                listMatriculaJoin = selectMatricula.listarMatriculaCurso();
                Tablas.listarMatriculasCurso(listMatriculaJoin, TbaMatriculados, curso, paralelo);

            } catch (Exception e) {
            }
        }
        if (pos == 5) {
            //TODO
            listMatric = mc.findMatriculaEntities();
            Tablas.listarMatriculasTodo(listMatric, TbaMatriculados);

        }
        if (pos == 6) {
            //PERIODO LECTIVO Y CURSO 
            String periodo = null;
            String curso = null;
            String paralelo = null;
//            Long id_aula = null;
            String[] arrSplit_2 = query.split(" ", 3);
            for (int i = 0; i < arrSplit_2.length; i++) {
                periodo = arrSplit_2[0];
                curso = arrSplit_2[1];
                paralelo = arrSplit_2[2];
            }
//                System.out.println("periodo"+periodo);//
//                System.out.println("curso"+curso);//
//                System.out.println("paralelo"+paralelo);//
            Paralelos paralelos = ObtenerDTO.ObtenerParalelos(paralelo);
            Cursos cursos = ObtenerDTO.ObtenerCursos(curso);
//            System.out.println("parametros1:"+paralelos.getNombre());
//            System.out.println("parametros2:"+cursos.getNombre());
            RelCursoParalelo id_aula = ObtenerDTO.ObtenerRelCursosParalelos(cursos.getIdCursos(), paralelos.getIdParalelos());
//            System.out.println("envio;"+periodo+id_aula.getIdRelCursoParalelo());
            listMatriculaJoin = selectMatricula.listarMatriculaPeriodoLectivoCurso(periodo, id_aula.getIdRelCursoParalelo());
            Tablas.listarMatriculasEstudiante(listMatriculaJoin, TbaMatriculados);

        }

        txtBuscar.setText("");

//        Tablas.cargarFiltroProductosNota(t_Nota_faltantes, lista);
        query = "";
    }

