    private void TbaMatriculadosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbaMatriculadosMousePressed
        int id = 0;
        if (evt.getClickCount() == 2) {

            id = TbaMatriculados.getSelectedRow();

            objMatri = devuelveObjeto(Long.valueOf(TbaMatriculados.getValueAt(id, 0).toString()), listMatric);
            if (objMatri != null) {

                ConsultaDetalleMatricula au = new ConsultaDetalleMatricula(new javax.swing.JFrame(), true, objMatri);
                au.setVisible(true);

            }
            cargarTbaMatriculados();
        }
    }//GEN-LAST:event_TbaMatriculadosMousePressed
    public Matricula devuelveObjeto(Long id, List<Matricula> listabod) {
        Matricula doc = null;
        for (int i = 0; i < listabod.size(); i++) {
            if (listabod.get(i).getIdMatricula().equals(id)) {
                doc = listabod.get(i);
                break;
            }
        }
        return doc;
    }
