    public model_seg_proveedor_join devuelveObjetoEmpleado(String datos, ArrayList<model_seg_proveedor_join> listarobj) {
        model_seg_proveedor_join objeto1 = null;
        for (int i = 0; i < listarobj.size(); i++) {
            if (datos.equals(listarobj.get(i).getId_usuario().toString())) {
                objeto1 = listarobj.get(i);
                break;
            }
        }
        return objeto1;
    }


    private void jtTablaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtTablaMousePressed
        int i = 0;
        try {
            if (evt.getClickCount() == 2) {
                i = jtTabla.getSelectedRow();
                tabObjetoProveedor = devuelveObjetoEmpleado(jtTabla.getValueAt(i, 0).toString(), listar);
                if (tabObjetoProveedor != null) {
                    System.out.println("holaaaaa");
                    FrmProveedorUpdate acc = new FrmProveedorUpdate(new javax.swing.JFrame(), true, objUsuario, objEmpresa, tabObjetoProveedor);
                    acc.setVisible(true);
                    listar.clear();
                    tabla();
                }
            }
        } catch (Exception e) {
            Logger.getLogger(FrmProveedorList.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_jtTablaMousePressed
    
    private void txtBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyTyped
        char c = evt.getKeyChar();
        if (Character.isLowerCase(c)) {
            String cad = ("" + c).toUpperCase();
            c = cad.charAt(0);
            evt.setKeyChar(c);
        }
    }//GEN-LAST:event_txtBuscarKeyTyped

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int i = jtTabla.getSelectedRow();
        if (jtTabla.isRowSelected(i) == false) {
            JOptionPane.showMessageDialog(this, "SELECCIONE UN REGISTRO");
        } else {
            tabObjetoProveedor = devuelveObjetoEmpleado(jtTabla.getValueAt(i, 0).toString(), listar);
            if (tabObjetoProveedor != null) {
                System.out.println("holaaaaa");
                FrmProveedorUpdate acc = new FrmProveedorUpdate(new javax.swing.JFrame(), true, objUsuario, objEmpresa, tabObjetoProveedor);
                acc.setVisible(true);
                listar.clear();
                tabla();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

