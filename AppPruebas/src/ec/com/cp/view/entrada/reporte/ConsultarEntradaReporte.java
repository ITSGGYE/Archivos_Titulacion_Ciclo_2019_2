public model_co_cabecera_compra devuelveObjeto(String datos, ArrayList<model_co_cabecera_compra> listarobj) {
        model_co_cabecera_compra objeto1 = null;
        for (int i = 0; i < listarobj.size(); i++) {
            if (datos.equals(listarobj.get(i).getId_cabecera().toString())) {
                objeto1 = listarobj.get(i);
                break;
            }
        }
        return objeto1;
    }
