    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ArrayList tablac = new ArrayList();
        try {
        for (int i = 0; i < tabla.getRowCount(); i++) {
//            id = Long.valueOf(tabla.getValueAt(i, 0).toString());

            Inventario tabla1 = new Inventario(""+tabla.getValueAt(i, 0).toString(),
                    ""+tabla.getValueAt(i, 3).toString()/*2CANT*/,""+costo.toString(),
                    ""+venta.toString(), "" + tabla.getValueAt(i, 2).toString()/*5PRO*/,
                    ""+total.toString(), ""+tabla.getValueAt(i, 4).toString()/*7COSTO*/,
                    ""+tabla.getValueAt(i, 5).toString()/*8VENTA*/, ""+tabla.getValueAt(i, 6).toString()/*9DESC*/,
                    ""+tabla.getValueAt(i, 9).toString()/*10BOD*/);
            tablac.add(tabla1);

        }
        
            String dir = System.getProperty("user.dir") + "/src/" + "/ec/" + "/com/" + "/cp/" + "/view/" + "/inventario/"
                   +"/report/" + "InventarioReport.jasper";
            JasperReport reporte = (JasperReport) JRLoader.loadObject(dir);
            JasperPrint jprint = JasperFillManager.fillReport(reporte, null, new JRBeanCollectionDataSource(tablac));
            JDialog frame = new JDialog(this);
            JRViewer viewer = new JRViewer(jprint);
            frame.add(viewer);
            frame.setSize(new Dimension(ancho / 2, 500));
            frame.setLocationRelativeTo(null);
            frame.setVisible(true);
            viewer.setFitWidthZoomRatio();
        } catch (JRException ex) {
            Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

