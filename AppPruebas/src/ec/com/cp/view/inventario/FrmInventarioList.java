    public model_inv_inventario_join devuelveObjeto(Long id, ArrayList<model_inv_inventario_join> lista) {
        model_inv_inventario_join doc = null;
        for (int i = 0; i < lista.size(); i++) {
            System.out.println("kardex: "+lista.get(i).getId_kardex());
            if (Objects.equals(lista.get(i).getId_producto(), id)) {
                doc = lista.get(i);
                break;
            }
        }
        return doc;
    }
