    public void cargarTotalSubtotal() {
        Double subtotal = 0.00;
        for (int i = 0; i < tabla.getRowCount(); i++) {
            Double tabla1 = Double.valueOf(tabla.getValueAt(i, 5).toString().replace(',', '.'));
            subtotal += tabla1;
        }
        venta = subtotal;
        txt_venta.setText("$ " + Formato_Numeros.formatoNumero(venta.toString()));
    }

    public void cargarTotalIva() {
        Double iva = 0.00;
        for (int i = 0; i < tabla.getRowCount(); i++) {
            Double tabla1 = Double.valueOf(tabla.getValueAt(i, 4).toString().replace(',', '.'));
            Double descuento = Double.valueOf(tabla.getValueAt(i, 6).toString().replace(',', '.'));
            iva += tabla1 - descuento;
        }
        costo = iva;
        txt_costo.setText("$ " + Formato_Numeros.formatoNumero(costo.toString()));
        
        
        total = venta - costo;
        txt_total.setText("$ " + Formato_Numeros.formatoNumero(total.toString()));
    }
    
