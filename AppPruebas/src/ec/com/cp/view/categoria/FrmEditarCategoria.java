    public void proceso() {
        ArrayList<model_pdt_subcategoria_join_subcategoria> lista = new ArrayList<>();
        lista = control.listarSubCategorias(1);
        Long idSub;
        ArrayList<model_pdt_marca_dos> listMarca = ctrMarca.listarMarcas(1);
        Long idMar;
        ArrayList<model_pdt_articulo_dos> listArticulo = ctrArt.listarArticulos(1);
        Long idArt;
        ArrayList<model_pdt_producto> listPro = ctrPro.listarProducto(1);
        Long idPro;

        for (int i = 0; i < lista.size(); i++) {
            if (Objects.equals(lista.get(i).getId_categoria(), categoria.getId_categoria())) {

                idSub = lista.get(i).getId_subcategoria();
                subcategoria.setId_subcategoria(idSub);
                subcategoria.setEstado(estado.charAt(0));
                subcategoria.setUsuario_actulizacion("YO");

                String msgSub = crudSubCat.activarInactivarSubCategoria(subcategoria);
                System.out.println("id sub: " + idSub + estado.charAt(0));
                for (int m = 0; m < listMarca.size(); m++) {
                    if (Objects.equals(listMarca.get(m).getId_subcategoria(), idSub)) {

                        idMar = listMarca.get(m).getId_marca();
                        marca.setId_marca(idMar);
                        marca.setEstado(estado.charAt(0));
                        marca.setU_actualizacion("YO");

                        String msgMar = ctrMarca.activarInactivarMarca(marca);
                        System.out.println("id marca: " + listMarca.get(m).getId_marca());
                        for (int a = 0; a < listArticulo.size(); a++) {
                            if (Objects.equals(listArticulo.get(a).getId_marca(), idMar)) {

                                idArt = listArticulo.get(a).getId_articulo();
                                articulo.setId_articulo(idArt);
                                articulo.setEstado(estado.charAt(0));
                                articulo.setUsuario_actulizacion("YO");

                                String msgArt = ctrArt.activarInactivarArticulo(articulo);
                                System.out.println("id articulo: " + listArticulo.get(a).getId_articulo());
                                for (int p = 0; p < listPro.size(); p++) {
                                    if (Objects.equals(listPro.get(p).getId_articulo(), idArt)) {

                                        idPro = listPro.get(p).getId_producto();
                                        producto.setId_producto(idPro);
                                        producto.setEstado(estado.charAt(0));
                                        producto.setUsuario_actulizacion("YO");

                                        String msgPro = ctrPro.activarInactivarProducto(producto);
                                        System.out.println("id producto: " + idPro);
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
    }
