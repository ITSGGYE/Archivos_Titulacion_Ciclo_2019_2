    public void activaInactivar() {
        String boton = btnBoton.getText();
        if ("INACTIVAR".equals(boton) || "ACTIVAR".equals(boton)) {
            JOptionPane.showMessageDialog(this, "RECUERDE QUE SE " + boton + "A EN CASCADA TODA LO CONTENIDO EN ESTA CATEGORIA!");
            int r = JOptionPane.showConfirmDialog(null, "Â¿ESTA SEGURO DE " + boton + " A: " + categoria.getNombre() + "?", "", JOptionPane.YES_NO_OPTION);
            if (r == JOptionPane.YES_OPTION) {

                if ("INACTIVAR".equals(boton)) {
                    estado = "I";
                }
                if ("ACTIVAR".equals(boton)) {
                    estado = "A";
                }
                categoria.setId_categoria(categoria.getId_categoria());
                categoria.setEstado(estado.charAt(0));
                categoria.setUsuario_actulizacion("YO");
                try {
                    String msg = crud.activarInactivarCategoria(categoria);
                    proceso();
                    JOptionPane.showMessageDialog(this, msg);


                } catch (HeadlessException e) {
                    JOptionPane.showMessageDialog(this, e);
                }
                setVisible(false);
            } else {
            }
        }
    }
