    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        java.util.Date date = new java.util.Date();

        java.sql.Date sqlDate = new java.sql.Date(date.getTime());
        java.sql.Timestamp sqlTime = new java.sql.Timestamp(date.getTime());

        try {
            categoria.setNombre(txt_categoria.getText());
            categoria.setEstado('A');
            categoria.setUsuario_creacion("YO");
            categoria.setF_creacion(sqlTime);

            String o = crud.insertarCategoria(categoria, 1);
            if (o.equals("GUARDADO EXITOSAMENTE")) {
                JOptionPane.showMessageDialog(null, o);
                setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, o);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

